int whichBw(int ch);

struct Link {
    int _idR, _idS;
    int id;
    int ch;
    int bw;
    double distanceSenderReceiver;
    double interference;
    double SINR;
    int MCS;

    Link(int id_, int ch_) : id(id_), ch(ch_) {
      _idR = _idS = id_;
      bw = whichBw(ch_);
    }
};


